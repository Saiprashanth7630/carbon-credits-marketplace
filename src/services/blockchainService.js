// Ethereum provider from MetaMask
const provider = new ethers.providers.Web3Provider(window.ethereum);

// Prompt user for wallet access
const requestAccount = async () => {
    const accounts = await provider.send('eth_requestAccounts', []);
    return accounts[0];
};

// ðŸ“ˆ Get current carbon credit price (off-chain from backend API)
export const getCreditPrice = async () => {
    try {
        const response = await axios.get(`${API_GATEWAY_URL}/price`);
        return response.data.price;
    } catch (error) {
        throw new Error('Failed to fetch credit price');
    }
};

// ðŸ’° Purchase credits (blockchain interaction)
export const purchaseCredits = async (amount) => {
    try {
        await requestAccount();
        const signer = provider.getSigner();
        const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
        const amountInWei = ethers.utils.parseUnits(amount.toString(), 18);

        const tx = await contract.purchase(await signer.getAddress(), amountInWei);
        await tx.wait();

        return { success: true, transactionHash: tx.hash };
    } catch (error) {
        console.error(error);
        throw new Error('Failed to purchase credits');
    }
};

// ðŸ” Transfer credits to another wallet
export const transferCredits = async (recipient, amount) => {
    try {
        await requestAccount();
        const signer = provider.getSigner();
        const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);
        const amountInWei = ethers.utils.parseUnits(amount.toString(), 18);

        const tx = await contract.transfer(recipient, amountInWei);
        await tx.wait();

        return { success: true, transactionHash: tx.hash };
    } catch (error) {
        console.error(error);
        throw new Error('Failed to transfer credits');
    }
};

// ðŸ“Š Get balance of credits for a user address
export const getBalance = async (address) => {
    try {
        const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, provider);
        const balance = await contract.balanceOf(address);
        return ethers.utils.formatUnits(balance, 18);
    } catch (error) {
        console.error(error);
        throw new Error('Failed to fetch balance');
    }
};

